'''–î–µ—Å–∫—Ä–∏–ø—Ç–æ—Ä, –∫–æ–Ω—Ç—Ä–æ–ª–∏—Ä—É—é—â–∏–π –∑–Ω–∞—á–µ–Ω–∏—è –≤ –∑–∞–¥–∞–Ω–Ω–æ–º –¥–∏–∞–ø–∞–∑–æ–Ω–µ'''
class BoundedStat:
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è –Ω–∞—á–∞–ª—å–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    def __init__ (self, name, min_value=0, max_value=100):
        # –ò–º—è –∞—Ç—Ä–∏–±—É—Ç–∞
        self.name = name
        # –ú–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
        self.min_value = min_value
        # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
        self.max_value = max_value

    # –ú–µ—Ç–æ–¥ __set__ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –ø—Ä–∏—Å–≤–æ–µ–Ω–∏—è –∑–Ω–∞—á–µ–Ω–∏—è –∞—Ç—Ä–∏–±—É—Ç—É –æ–±—ä–µ–∫—Ç–∞
    def __set__ (self, instance, value):
        # –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –º–µ–Ω—å—à–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω–æ–≥–æ –º–∏–Ω–∏–º—É–º–∞ - —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è –º–∏–Ω–∏–º—É–º
        if value < self.min_value:
            value = self.min_value
        # –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –±–æ–ª—å—à–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω–æ–≥–æ –º–∞–∫—Å–∏–º—É–º–∞ - —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è –º–∞–∫—Å–∏–º—É–º
        elif value > self.max_value:
            value = self.max_value
        # –°–æ—Ö—Ä–∞–Ω—è–µ—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏–µ –≤ —Å–ª–æ–≤–∞—Ä–µ –∞—Ç—Ä–∏–±—É—Ç–æ–≤ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞
        instance.__dict__[self.name] = value

'''–†–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–π –∫–ª–∞—Å—Å –¥–ª—è –≤—Å–µ—Ö –ø–µ—Ä—Å–æ–Ω–∞–∂–µ–π –∏–≥—Ä—ã'''
class Human:
    # –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–µ—Å–∫—Ä–∏–ø—Ç–æ—Ä –¥–ª—è –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è –∑–¥–æ—Ä–æ–≤—å—è –∏ –º–∞–Ω—ã
    hp = BoundedStat('hp', 0, 100)
    mp = BoundedStat('mp', 0, 100)

    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è –Ω–∞—á–∞–ª—å–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    def __init__(self, name, strength, iq, damage, hp=100, mp=100):
        self.name = name              # –ò–º—è –ø–µ—Ä—Å–æ–Ω–∞–∂–∞
        self.strength = strength      # –°–∏–ª–∞
        self.iq = iq                  # –ò–Ω—Ç–µ–ª–ª–µ–∫—Ç
        self.damage = damage          # –£—Ä–æ–Ω
        self.hp = hp                  # –ó–¥–æ—Ä–æ–≤—å–µ
        self.mp = mp                  # –ú–∞–Ω–∞
        self.artifacts = []           # –°–ø–∏—Å–æ–∫ –∞—Ä—Ç–µ—Ñ–∞–∫—Ç–æ–≤

    # –ú–µ—Ç–æ–¥ –ø–æ–ª—É—á–µ–Ω–∏—è —É—Ä–æ–Ω–∞ –ø–µ—Ä—Å–æ–Ω–∞–∂–µ–º
    def take_damage(self, damage):
        self.hp -= damage

    # –ú–µ—Ç–æ–¥ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏—è –∑–¥–æ—Ä–æ–≤—å—è
    def regenerate(self, regen):
        self.hp += regen

    # –î–µ–∫–æ—Ä–∞—Ç–æ—Ä, –ø–æ–∑–≤–æ–ª—è—é—â–∏–π —É–∑–Ω–∞—Ç—å –∂–∏–≤ –ª–∏ –ø–µ—Ä—Å–æ–Ω–∞–∂
